/* AからBへ正方向に進んだ位置から「距離型(11種)」を求める */
function getShukuDistanceType(idA, idB) {
  const n = 27;
  const forward = ((Number(idB) - Number(idA)) % n + n) % n;
  return DISTANCE_TO_TYPE[forward];
}

/* 2つの宿ID(1-27)から「6分類」を求める */
function getShukuGroup6(idA, idB) {
  return TYPE_TO_GROUP6[getShukuDistanceType(idA, idB)];
}

